package com.jigar.otr.helper;

/**
 * Created by jigar.joshi on 11/23/16.
 */
public class MessagePreKeysCountResponse {
	private int count;

	public MessagePreKeysCountResponse(int count) {
		this.count = count;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	@Override
	public boolean equals(Object o) {
		if (this == o) return true;
		if (o == null || getClass() != o.getClass()) return false;

		MessagePreKeysCountResponse that = (MessagePreKeysCountResponse) o;

		return count == that.count;

	}

	@Override
	public int hashCode() {
		return count;
	}
}
